// Make Code
int64 move(int64 s, int64 con, int64 t, int64 n){
  if(s>t)
    swap(s, t);
  if(s < con and con < t)
    return n-(t-s);
  return t-s;
}

int main(){
  int64 n, q, t, ans=0;
  int64 nl=0, nr=1;
  char h;
  cin >> n >> q;
  rep(qi, q){
    cin >> h >> t;
    t--;
    switch (h){
    case 'L':
      ans += move(nl, nr, t, n);
      nl= t;
      break;

    case 'R':
      ans += move(nr, nl, t, n);
      nr= t;
      break;
    }
  }
  cout << ans << endl;
  return 0;
}