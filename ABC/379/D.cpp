int main(){
  int64 q, type, now=0;
  cin >> q;
  queue<int64> que;
  rep(qi, q){
    cin >> type;
    switch(type){
      case 1:
        que.push(now);
        break;
      case 2:
        int64 t;
        cin >> t;
        now += t;
        break;
      case 3:
        int64 h, ans=0;
        cin >> h;
        while(que.size() and now-que.front()>=h){
          que.pop();
          ans++;
        }
        cout << ans << endl;
        break;
    }
  }
  return 0;
}